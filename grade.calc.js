// Grade calculator

var marks = +prompt("write your marks?");

if (marks >= 90) {
    console.log("grade A");
} else if (marks >= 80) {
    console.log("grade B");
} else if (marks >= 70) {
    console.log("grade c");
} else if (marks >= 60) {
    console.log("grade d");
} else if (marks >= 50) {
    console.log("grade e");
} else {
    console.log("Fail");
}